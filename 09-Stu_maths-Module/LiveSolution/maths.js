function add(num1, num2) {
  // return the sum
  return num1 + num2;
}

function subtract(num1, num2) {
  // return the difference
  return num1 - num2;
}

function multiply(num1, num2) {
  // return the product
  return num1 * num2;
}

function divide(num1, num2) {
  // return the quotient
  return num1 / num2;
}

module.exports = {
  add: add,
  subtract: subtract,
  multiply: multiply,
  divide: divide,
};
